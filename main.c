int screen(int width, int height, int fullscreen, const char *title,
           SDL_Window **window, SDL_Renderer **renderer) {
  if (SDL_Init(SDL_INIT_VIDEO) != 0) {
    printf("SDL_Init Error: %s\n", SDL_GetError());
    return 1;
  }

  Uint32 flags = fullscreen ? SDL_WINDOW_FULLSCREEN : 0;

  *window = SDL_CreateWindow(title, SDL_WINDOWPOS_CENTERED,
                             SDL_WINDOWPOS_CENTERED, width, height, flags);
  if (*window == NULL) {
    printf("SDL_CreateWindow Error: %s\n", SDL_GetError());
    SDL_Quit();
    return 1;
  }

  *renderer = SDL_CreateRenderer(
      *window, -1, SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);
  if (*renderer == NULL) {
    SDL_DestroyWindow(*window);
    printf("SDL_CreateRenderer Error: %s\n", SDL_GetError());
    SDL_Quit();
    return 1;
  }

  return 0;
}

int done() {
  SDL_Event event;
  while (SDL_PollEvent(&event)) {
    if (event.type == SDL_QUIT) {
      return 1;
    }
  }
  return 0;
}

int main(int argc, char *argv[]) {
  double posX = 22, posY = 12;      // x and y start position
  double dirX = -1, dirY = 0;       // initial direction vector
  double planeX = 0, planeY = 0.66; // the 2d raycaster version of camera plane

  double time = 0;    // time of current frame
  double oldTime = 0; // time of previous frame

  SDL_Window *window = NULL;
  SDL_Renderer *renderer = NULL;

  if (screen(screenWidth, screenHeight, 0, "Raycaster", &window, &renderer) !=
      0) {
    return 1;
  }
  while (!done()) {
    SDL_SetRenderDrawColor(renderer, 0, 0, 0, 255); // set draw color to black
    SDL_RenderClear(renderer);                      // Clear the screen

    for (int x = 0; x < screenWidth; x++) {
      // calculate ray position and direction
      double cameraX =
          2 * x / (double)screenWidth - 1; // x-coordinate in camera space
      double rayDirX = dirX + planeX * cameraX;
      double rayDirY = dirY + planeY * cameraX;

      // which box of the map we're in
      int mapX = (int)posX;
      int mapY = (int)posY;

      // length of ray from current position to next x or y side
      double sideDistX;
      double sideDistY;

      // length of ray from one x or y-side to next x or y-side
      double deltaDistX = (rayDirX == 0) ? 1e30 : fabs(1 / rayDirX);
      double deltaDistY = (rayDirY == 0) ? 1e30 : fabs(1 / rayDirY);

      // what direction to step in x or y-direction (either +1 or -1)
      int stepX;
      int stepY;

      int hit = 0; // was there a wall hit?
      int side;    // was there a NS or a EW wall hit?
      // calculate step and initial sideDist
      if (rayDirX < 0) {
        stepX = -1;
        sideDistX = (posX - mapX) * deltaDistX;
      } else {
        stepX = 1;
        sideDistX = (mapX + 1.0 - posX) * deltaDistX;
      }
      if (rayDirY < 0) {
        stepY = -1;
        sideDistY = (posY - mapY) * deltaDistY;
      } else {
        stepY = 1;
        sideDistY = (mapY + 1.0 - posY) * deltaDistY;
      }
    }

    SDL_RenderPresent(renderer); // update the screen
  }
}
